    void LoadTable() {
        defaultTableModel = (DefaultTableModel) tb_chatLieu.getModel();
        defaultTableModel.setRowCount(0);
        for (CHATLIEU cl : cls.getListKT()) {
            defaultTableModel.addRow(new Object[]{
                cl.getMaChatLieu(),cl.getTenChatLieu(),cl.getNgayTao(),
                cl.getNgaySua(),cl.getTrangThai() == 1 ? "Xau" : "Dep"
            });
        }
    }
    
    private CHATLIEU getData() {
//        int maKichThuoc = Integer.parseInt(txt_maKichThuoc.getText().trim());
        String kichThuoc = txt_tenChatLieu.getText().trim();
        String NgayTao = txt_ngayTao.getText().trim();
        String NgaySua = txt_ngaySua.getText().trim();
        Date ngayTao = (Date.valueOf(NgayTao));
        Date ngaySua = (Date.valueOf(NgaySua));
        int trangThai = TrangThai();
        if (kichThuoc.length() == 0 || NgaySua.length() == 0 || NgaySua.length() == 0) {
            JOptionPane.showMessageDialog(this, "Không để trống");
            return null;
        }
        CHATLIEU chatlieu = new CHATLIEU(0, kichThuoc, ngayTao, ngaySua, trangThai);
        return chatlieu;
    }

    private int TrangThai() {
        if (cb_trangThai.getSelectedItem() == "Xau") {
            return 1;
        } else {
            return 0;
        }
    }

