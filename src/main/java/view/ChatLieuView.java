    private void btn_suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_suaActionPerformed
        // TODO add your handling code here:
        int row = tb_chatLieu.getSelectedRow();
        CHATLIEU chatlieu = getData();
        if (chatlieu == null) {
            return;
        }
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn dữ liệu");
            return;
        }
        String id = tb_chatLieu.getValueAt(row, 0).toString();
        int ID = (Integer.parseInt(id));
        String traLoi = cls.update(chatlieu, ID);
        LoadTable();
    }//GEN-LAST:event_btn_suaActionPerformed

    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        // TODO add your handling code here:
        int row = tb_chatLieu.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn dữ liệu");
            return;
        }
        String id = tb_chatLieu.getValueAt(row, 0).toString();
        int ID = (Integer.parseInt(id));
        String traLoi = cls.delete(ID);
        JOptionPane.showMessageDialog(this, traLoi);
        LoadTable();
    }//GEN-LAST:event_btn_xoaActionPerformed

