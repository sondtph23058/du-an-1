    private void Loadtabale() {
        defaultTableModel = (DefaultTableModel) tb_bang.getModel();
        defaultTableModel.setRowCount(0);
        for (MauSac mausac : mauSacService.getlistMauSac()) {
            defaultTableModel.addRow(new Object[]{
                mausac.getMaMauSac(), mausac.getTenMauSac(), mausac.getNgayTao(), mausac.getNgaySua(), mausac.getTrangThai() == 1 ? "Còn" : "Hết"
            });
        }
    }

    private int TrangThai() {
        if (cb_trangthai.getSelectedItem() == "Còn") {
            return 1;
        } else {
            return 0;
        }
    }

    private MauSac getForm() {

        String maMS = lbl_ma.getText().trim();
        String tenMS = txt_ten.getText().trim();
        String NgayTao = txt_ngaytao.getText().trim();
        String NgaySua = txt_ngaysua.getText().trim();
        Date ngayTao = (Date.valueOf(NgayTao));
        Date ngaySua = (Date.valueOf(NgaySua));
        int trangThai = TrangThai();

        if (maMS.length() == 0 || tenMS.length() == 0 || NgaySua.length() == 0 || NgaySua.length() == 0) {
            JOptionPane.showMessageDialog(this, "Không để trống");
            return null;
        }
        MauSac mauSac = new MauSac(0, tenMS, ngayTao, ngaySua, trangThai);
        return mauSac;
    }

