    private void btn_suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_suaActionPerformed
        // TODO add your handling code here:
        int row = tb_bang.getSelectedRow();
        MauSac ms = getForm();
        if (ms == null) {
            return;
        }
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn dữ liệu");
            return;
        }
        String id = lbl_ma.getText().trim();
        int ID = (Integer.parseInt(id));
        String traLoi = mauSacService.update(ms, ID);
        Loadtabale();
        JOptionPane.showMessageDialog(this, traLoi);
    }//GEN-LAST:event_btn_suaActionPerformed

    private void tb_bangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_bangMouseClicked
        // TODO add your handling code here:
        int row = tb_bang.getSelectedRow();
        lbl_ma.setText(tb_bang.getValueAt(row, 0).toString());
        txt_ten.setText(tb_bang.getValueAt(row, 1).toString());
        txt_ngaytao.setText(tb_bang.getValueAt(row, 2).toString());
        txt_ngaysua.setText(tb_bang.getValueAt(row, 3).toString());
        cb_trangthai.setSelectedItem(tb_bang.getValueAt(row, 4).toString());

    }//GEN-LAST:event_tb_bangMouseClicked

    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        int row = tb_bang.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn dữ liệu");
            return;
        }
        String id = tb_bang.getValueAt(row, 0).toString();
        int ID = (Integer.parseInt(id));
        String traLoi = mauSacService.delete(ID);
        Loadtabale();
        JOptionPane.showMessageDialog(this, traLoi);        // TODO add your handling code here:
    }//GEN-LAST:event_btn_xoaActionPerformed

