    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        CLB cv = getForm();
        if (cv == null) {
            return;
        }
        String traLoi = clbs.addCLB(cv);
        JOptionPane.showMessageDialog(this, traLoi);
        loadTable();
    }//GEN-LAST:event_btnThemActionPerformed

    private void tbChucVuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbChucVuMouseClicked
        int row = tbChucVu.getSelectedRow();
        
        lb_MaCLB.setText(tbChucVu.getValueAt(row, 0).toString());
        cbMTT.setSelectedItem(tbChucVu.getValueAt(row, 1).toString());
        txtTenCLB.setText(tbChucVu.getValueAt(row, 2).toString());
        txtNgayTao.setText(tbChucVu.getValueAt(row, 3).toString());
        txtNgaySua.setText(tbChucVu.getValueAt(row, 4).toString());
        cb_TrangThai.setSelectedItem(tbChucVu.getValueAt(row, 5).toString());
    }//GEN-LAST:event_tbChucVuMouseClicked

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int row = tbChucVu.getSelectedRow();
        CLB cv = getForm();
        if (cv == null) {
            return;
        }
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn dữ liệu");
            return;
        }
        String id = tbChucVu.getValueAt(row, 0).toString();
        int ID = (Integer.parseInt(id));
        String traLoi = clbs.updateCLB(cv, ID);
        loadTable();
        JOptionPane.showMessageDialog(this, traLoi);
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int row = tbChucVu.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn dữ liệu");
            return;
        }
        String id = tbChucVu.getValueAt(row, 0).toString();
        int ID = (Integer.parseInt(id));
        String traLoi = clbs.deleteCLB(ID);
        loadTable();
        JOptionPane.showMessageDialog(this, traLoi);
    }//GEN-LAST:event_btnXoaActionPerformed

