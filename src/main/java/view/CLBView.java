    private void loadCBMTT() {
        defaultComboBoxModel = (DefaultComboBoxModel) cbMTT.getModel();
        for (MONTHETHAO string : mtts.getListMTT()) {
            defaultComboBoxModel.addElement(string.getMAMTT());
        }

    }

    private void loadCB() {
        defaultComboBoxModel = (DefaultComboBoxModel) cb_TrangThai.getModel();
        ArrayList<String> list = new ArrayList<>();
        list.add("Hoạt Động");
        list.add("Không Hoạt Động");
        for (String string : list) {
            defaultComboBoxModel.addElement(string);
        }

    }

    private void loadTable() {
        defaultTableModel = (DefaultTableModel) tbChucVu.getModel();
        defaultTableModel.setRowCount(0);
        for (CLB cv : clbs.getListCLB()) {
            defaultTableModel.addRow(new Object[]{
                cv.getMACLB(), cv.getMAMTT(), cv.getTENCLB(), cv.getNGAYTAO(), cv.getNGAYSUA(), cv.getTRANGTHAI() == 1 ? "Hoạt Động" : "Không Hoạt Động"});
        }
    }

    private int TrangThai() {
        if (cb_TrangThai.getSelectedItem() == "Hoạt Động") {
            return 1;
        } else {
            return 0;
        }
    }

    private CLB getForm() {
        String tenCV = txtTenCLB.getText().trim();
        String NgayTao = txtNgayTao.getText().trim();
        String NgaySua = txtNgaySua.getText().trim();
        Date ngayTao = (Date.valueOf(NgayTao));
        Date ngaySua = (Date.valueOf(NgaySua));
        int trangThai = TrangThai();
        String idMa = cbMTT.getSelectedItem().toString();
        if (tenCV.length() == 0 || NgaySua.length() == 0 || NgaySua.length() == 0) {
            JOptionPane.showMessageDialog(this, "Không để trống");
            return null;
        }
        CLB cv = new CLB(0, Integer.parseInt(idMa), tenCV, ngayTao, ngaySua, trangThai);
        return cv;
    }

